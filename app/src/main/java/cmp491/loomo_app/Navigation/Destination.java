package cmp491.loomo_app.Navigation;

public class Destination {
    public int x;
    public int y;
    public float thetha;

    public Destination() {
    }

    public Destination(int x, int y, float thetha) {
        this.x = x;
        this.y = y;
        this.thetha = thetha;
    }

    public Destination(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
